class Solution {
  public:
    void printTriangle(int n) {
        // code here
        for(int i=1;i<=n;i++) { //for total number of rows
            for(int j=1; j<=i;j++) { //for innner values of rows
                cout<<j<<" ";
            }
            cout<<endl;
        }
    }
};

